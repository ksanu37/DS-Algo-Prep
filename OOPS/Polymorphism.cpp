/* Polymorphism : The word polymorphism means having many forms. In simple words, we can define polymorphism as 
the ability of a message to be displayed in more than one form.


Two types:
		1. Compile time polymorhism
		2. Run time Polymorphism
		


1. Compile time polymorphism:

		A. Function Overloading
		B. Operator overloading

2. Run time polymorphism:
		Virtual Functions (Function Overriding)
		

Abstract Class: A class having at least one pure virtual function is an abstract class. 

An Abstract class can't be instantiated.  An abstract class with only pure virtual functions  --> Interface

** We can have pointers and references to an abstract class --> can point to derived class objects
** If the derived class doesn't override the pure virtual function, it becomes an abstract class itself
** An Abstract class can have constructors.
		
*/
